#include <iostream>
using namespace std;
int main()
{
    int a;
    float b;
    char c;
    bool d;
    long int e;
    short int f;
    cout << "Size of int is " << sizeof(a) << " bytes" << endl;
    cout << "Size of float is " << sizeof(b) << " bytes" << endl;
    cout << "Size of char is " << sizeof(c) << " byte" << endl;
    cout << "Size of bool is " << sizeof(d) << " byte" << endl;
    cout << "Size of long int is " << sizeof(e) << " bytes" << endl;
    cout << "Size of short int is " << sizeof(f) << " bytes" << endl;
}
